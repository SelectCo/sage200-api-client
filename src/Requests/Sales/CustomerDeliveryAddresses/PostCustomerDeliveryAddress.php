<?php

namespace Selectco\SageApi\Requests\Sales\CustomerDeliveryAddresses;

use Saloon\Contracts\Body\HasBody;
use Saloon\Enums\Method;
use Saloon\Http\Request;
use Saloon\Traits\Body\HasJsonBody;

/**
 * PostCustomerDeliveryAddress
 *
 * Customer delivery address for sales order to add.
 */
class PostCustomerDeliveryAddress extends Request implements HasBody
{
	use HasJsonBody;

	protected Method $method = Method::POST;
	private string $endPoint;


	public function __construct()
	{
		$this->endPoint = "/customer_delivery_addresses";
	}


	/**
	 * Define the endpoint for the request.
	 */
	public function resolveEndpoint(): string
	{
		return $this->endPoint;
	}
}
