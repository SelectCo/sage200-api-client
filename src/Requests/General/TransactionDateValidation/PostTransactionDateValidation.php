<?php

namespace Selectco\SageApi\Requests\General\TransactionDateValidation;

use Saloon\Contracts\Body\HasBody;
use Saloon\Enums\Method;
use Saloon\Http\Request;
use Saloon\Traits\Body\HasJsonBody;

/**
 * PostTransactionDateValidation
 *
 * Use this API endpoint to validate a date for a transaction for a given ledger.
 */
class PostTransactionDateValidation extends Request implements HasBody
{
	use HasJsonBody;

	protected Method $method = Method::POST;
	private string $endPoint;


	public function __construct()
	{
		$this->endPoint = "/transaction_date_validation";
	}


	/**
	 * Define the endpoint for the request.
	 */
	public function resolveEndpoint(): string
	{
		return $this->endPoint;
	}
}
